public class FindingPrimeNumber {

	public static void main(String[] args) {
		int input = 17;
		//boolean bol = false;
		
		for(int i=0;i<=input;i++)
			
		{
			if(i%2!=0)
			{
				System.out.print(i  +" is prime "+",");
			}
		
			else
			
				System.out.print(i  +" is even "+",");
			
		
			
		}
				
		}
}
			
		
		

	
